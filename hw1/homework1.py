a = int(input("type the first number (A)"))
b = int(input("type the second number (B)"))
c = int(input("type the third number (C)"))
print("\n")

if a + b == c:
    print ("yes, the third number is the sum of the first one and the second one")
else:
    print ("no, the third number is NOT the sum of the first one and the second one")
if a / b == c:
    print ("yes, the third number is the quotient of the first one and the second one")
else:
    print ("no, the third number is NOT the quotient of the first one and the second one")
